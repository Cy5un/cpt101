#include <stdio.h>

int main()
{
    int a = 10, b = 2, answer;

    printf("The two integers selected are %d and %d\n", a, b);

    // addition
    answer = a + b;
    printf("The result is %d\n", answer);

    // subtraction
    answer = a - b;
    printf("The result is %d\n", answer);

    // multiplication
    answer = a * b;
    printf("The result is %d\n", answer);

    // division
    answer = a / b;
    printf("The result is %d\n", answer);
}